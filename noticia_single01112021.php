                            <div id="post-content" class="post-body single wow fadeInLeft animated">
                            <p style="text-align: justify;">La nueva aerol&iacute;nea RED Air cuenta con Certificado de Operador A&eacute;reo (AOC), que la reconoce internacionalmente como una aerol&iacute;nea regular, tanto por Organizaci&oacute;n de Aviaci&oacute;n Civil Internacional (OACI), la Asociaci&oacute;n Internacional de Transporte A&eacute;reo (IATA),&nbsp; como otros organismos multilaterales del sector de transporte a&eacute;reo.&nbsp;</p>
<p style="text-align: justify;">Esta licencia, gracias al Instituto Dominicano de Aviaci&oacute;n Civil (IDAC) y la Junta de Aviaci&oacute;n Civil (JAC), permitir&aacute; operar vuelos directos a diferentes destinos que, de acuerdo con las estad&iacute;sticas de pasajeros, son ciudades que tienen un tr&aacute;fico natural desde y hacia el aeropuerto internacional de Santo Domingo, lo cual ha permitido movilizar m&aacute;s de diez millones de pasajeros en los &uacute;ltimos cinco a&ntilde;os.&nbsp;</p>
<p style="text-align: justify;">&ldquo;La certificaci&oacute;n supone el inicio de un proyecto que lleva varios a&ntilde;os tomando forma y que se ha conseguido gracias a todo el grupo de colaboradores de RED Air&rdquo;, se&ntilde;ala H&eacute;ctor G&oacute;mez, director ejecutivo de RED Air, que est&aacute; formada por un grupo de empresarios de amplia trayectoria en el sector de aviaci&oacute;n civil y servicios aeroportuarios.&nbsp;</p>
<p style="text-align: justify;">La empresa dominicana RED Air cuenta con una flota de cuatro aeronaves McDonnell Douglas MD-81 y MD-82 y un personal de m&aacute;s de cincuenta empleados, grupo que comprende el personal administrativo y operativo, contando con cinco pilotos dominicanos, al igual que el personal de cabina tambi&eacute;n dominicano.&nbsp;</p>
<p style="text-align: justify;">&ldquo;Se han realizado inversiones significativas en activos, infraestructura, y talento humano, para dar inicio a nuestras operaciones regulares y la sostenibilidad de las mismas, para desarrollar una aerol&iacute;nea que perdure en el tiempo&rdquo;, sostiene G&oacute;mez.&nbsp;</p>
<p style="text-align: justify;">Para conseguir esta certificaci&oacute;n RED Air ha tenido que cumplir con las normativas legales vigentes frente a las autoridades, que incluyen formaci&oacute;n y capacitaci&oacute;n de personal operativo y administrativo, demostraciones de seguridad, 50 horas de vuelos demostrativos,&nbsp;&nbsp;</p>
<p style="text-align: justify;">comprobaci&oacute;n, ejercicios de emergencias y simuladores, aprobaci&oacute;n de manuales operativos y de mantenimiento, entre otros.&nbsp;</p>
<p style="text-align: justify;">&ldquo;Queremos ser parte de la recuperaci&oacute;n del turismo en Rep&uacute;blica Dominicana, un pa&iacute;s l&iacute;der en el mercado tur&iacute;stico del Caribe, que cuenta con modernas infraestructuras hoteleras y una variada oferta para sus visitantes&rdquo;, puntualiz&oacute; G&oacute;mez.&nbsp;</p>
<p style="text-align: justify;">&ldquo;Buscamos atender la necesidad de los diferentes tipos de viajeros, sean de negocio, familiares o vacacionales&rdquo;, concluy&oacute; G&oacute;mez.&nbsp;</p>                              
