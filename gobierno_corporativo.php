								<p> RED Air es pionera en el programa de Compliance Aeronáutico y espera que el cumplimiento sea el estándar. Nuestro enfoque evolucionado de la supervisión no sugiere que seamos fáciles con el cumplimiento. RED Air seguirá utilizando la aplicación de la ley, las normas y regulaciones, así como los manuales operativos en todo momento. Además, RED Air mantendrá una estricta responsabilidad por los comportamientos inapropiados de toma de riesgos y tendrá tolerancia cero para el comportamiento intencional o imprudente.  <br>
