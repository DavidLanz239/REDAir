								<p> Las horas de trabajo de los empleados, incluidas las consideraciones para el trabajo voluntario de horas extraordinarias y la concesión de licencias de cualquier forma, deben estar de acuerdo con las normas locales aplicables y/o las leyes nacionales en las estaciones internacionales de RED Air.  <br>
